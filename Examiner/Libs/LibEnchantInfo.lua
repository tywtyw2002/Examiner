LEI.ITEMLINK_PATTERN_ENCHANT    = "item:"..("[^:]*:"):rep(1).."(%d*)";

function LEI:GetEnchantInfo(link)
    local id = tonumber(link:match(LEI.ITEMLINK_PATTERN_ENCHANT));
    if (not id) or (id == 0) then
        return;
    end

    local enchantName = LEI.ENCHANT_IDS[id]
    if not enchantName then
        return
    end
    -- return
    return id, enchantName;
end